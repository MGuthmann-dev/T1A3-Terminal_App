module Type_Colour

case colour
    when normal
        cl_type = :whitesmoke
    when fire
        cl_type = :orangered
    when water
        cl_type = :dodgerblue
    when electric
        cl_type = :yellow
    when grass
        cl_type = :green
    when ice
        cl_type = :skyblue
    when fighting
        cl_type = :burlywood
    when poison
        cl_type = :slateblue
    when ground
        cl_type = :peru
    when flying 
        cl_type = :lightcyan
    when psychic
        cl_type = :darkorchid
    when bug
        cl_type = :yellowgreen
    when rock
        cl_type = :saddlebrown
    when ghost
        cl_type = :lavender
    when dragon
        cl_type = :conrnflowerblue